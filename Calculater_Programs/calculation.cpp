#include<iostream>
using namespace std;
int main()
{
    // declare variables
    int n1, n2;

    // take input from end-user
    cout << "Enter a number: ";
    cin >> n1;

    cout << "Enter a another number: ";
    cin >> n2;

    // addition of two number
    cout << n1 << " + " << n2 << " = " << n1 + n2 << endl;

    // subtraction of two number
    cout << n1 << " - " << n2 << " = " << n1 - n2 << endl;

    //  multiplication of two number
    cout << n1 << " * " << n2 << " = " << n1 * n2 << endl;

    // division of two number
    cout << n1 << " / " << n2 << " = " << n1 / n2 << endl;
    
    return 0;
}